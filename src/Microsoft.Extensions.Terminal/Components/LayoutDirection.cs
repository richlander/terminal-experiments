/// <summary>
/// Specifies the direction for arranging child components in a layout.
/// </summary>
public enum LayoutDirection
{
    /// <summary>
    /// Arrange children horizontally (left to right).
    /// </summary>
    Horizontal,

    /// <summary>
    /// Arrange children vertically (top to bottom).
    /// </summary>
    Vertical
}
